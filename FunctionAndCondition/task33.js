//Write a function to check if a number is positive, negative or zero.

function Check(num){
    if(num > 0 ){
        console.log("the number is positive",num)
    }
    else if(num < 0){
        console.log("the number is negative",num)
    }
    else{
        console.log("the number is zero")
    }

}
Check(-12)
